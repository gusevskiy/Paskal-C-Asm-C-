#include <stdio.h>

int main(){
    int i;
    int m[] = {2, 3, 5, 7, 11, 13, 17, 19, 21, 23};
    /* узнать размер массива можно поделив размер массива
       на размер его элемента */
    

    for(i = 0; i < sizeof(m) / sizeof(*m); i++) // *m = m[0] первый элемент
        printf("[%d] = %d\n", i, m[i]);
}